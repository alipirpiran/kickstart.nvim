-- Zoom / Restore window
local function zoom_toggle()
  if vim.t.zoomed then
    vim.cmd(vim.t.zoom_winrestcmd)
    vim.t.zoomed = false
  else
    vim.t.zoom_winrestcmd = vim.fn.winrestcmd()
    vim.cmd 'resize'
    vim.cmd 'vertical resize'
    vim.t.zoomed = true
  end
end

-- Create the command
vim.api.nvim_create_user_command('ZoomToggle', zoom_toggle, {})

